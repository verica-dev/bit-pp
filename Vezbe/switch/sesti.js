var letter = "A";
switch (letter) {
    case "A":
        console.log("Good job");
        break;


    case "B":
        console.log("Pretty good");
        break;
    case "C":
        console.log("Pass");
        break;
    case "D":
        console.log("Not so good");
        break;
    case "F":
        console.log("Failed");
        break;
    default:
        console.log("Unknown grade");
        break;








}